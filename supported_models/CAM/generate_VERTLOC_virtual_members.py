# Prepare dictionary to hold vnamelists corresponding to each grid type
noisefile_gtype_vnamelist_correspondence = {}
for fname in local_noise_pkl_flist:
    noisefile_gtype_vnamelist_correspondence[fname] = {}
    for gtype_xy in list_gtypes_xy:
        noisefile_gtype_vnamelist_correspondence[fname][gtype_xy] = {}
        for gtype_z in list_gtypes_z:
            noisefile_gtype_vnamelist_correspondence[fname][gtype_xy][gtype_z] = []
# --- End of dictionary preparations


# Sorting variables into different grid kinds
for vname in ens_complete_vary_list:

    msg = '    Checking grid type of %s' % vname
    collective_print(msg, comm)

    # 2D or 3D?
    dimsize = len(data_info_dict[vname]['shape'])

    # Horizontal grid type -- mass, u_stag, or v_stag?
    last_shp2d = data_info_dict[vname]['dimensions'][-2:]
    xy_gtype = iden_gtype[last_shp2d]

