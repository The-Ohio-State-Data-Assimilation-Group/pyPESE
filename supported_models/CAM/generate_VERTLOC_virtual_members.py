'''
    APPLY PYPESE ON ENSEMBLE-COMPLETE DATA
    This is an embarrassingly parallel process

    To limit re-calculations of the E-matrix used in PESE-GC, will perform calculations
    for one grid type at a time.
'''
collective_print('Applying Localized PESE-GC.', comm)


# Apply pyPESE on one kind of grid and noise type at a time.
for local_noise_fname in local_noise_pkl_flist:
    for gtype_xy in list_gtypes_xy:
        for gtype_z in list_gtypes_z:

            # Skip to next grid type if there are no member-varying variables with this 
            # kind of grid
            if len( noisefile_gtype_vnamelist_correspondence[local_noise_fname][gtype_xy][gtype_z] ) == 0:
                continue

            msg = '    Processing xy grid %s z grid %s variables -- %s' % (
                gtype_xy, gtype_z, str( noisefile_gtype_vnamelist_correspondence[local_noise_fname][gtype_xy][gtype_z] )
            )
            collective_print(msg, comm)

            # Generate ensemble-complete noise for this grid type
            ens_complete_noise = deepcopy(vertloc_noise_dict[local_noise_fname]['noise'])
            if gtype_z == 'mass':
                ens_complete_noise = (ens_complete_noise[:,1:] + ens_complete_noise[:,:-1])/2
            elif gtype_z == 'horizontal':
                ens_complete_noise = ens_complete_noise[:,surf_lvl_index][:,np.newaxis]

            msg = '    Shape of noise: %s ' % (str(ens_complete_noise.shape))
            collective_print( msg, comm)

            # Loop over all levels associated with this grid type
            for ilvl in range( ens_complete_noise.shape[1] ):

                # Generate E matrix
                E = compute_unlocalized_gaussian_resampling_coefficients_with_precomputed_noise(
                    fcst_ens_size, virt_ens_size, ens_complete_noise[:,ilvl]
                )
                E = np.matrix(E)

                # Loop over all xy locations associated with this grid type
                for iloc in range(my_xy_ind_dict[gtype_xy]['len']):

                    # Loop over all variables that use selected local_noise_fname and grid type
                    for vname in noisefile_gtype_vnamelist_correspondence[local_noise_fname][gtype_xy][gtype_z]:

                        # Grab ensemble for selected variable & grid location
                        if gtype_z == 'horizontal':
                            fcst_ens1d = ( deepcopy( my_ens_complete_data[vname][:fcst_ens_size,iloc] ) ).astype('f8')
                        else:
                            fcst_ens1d = ( deepcopy( my_ens_complete_data[vname][:fcst_ens_size,ilvl,iloc] ) ).astype('f8')
                        # --- End of data grabbing

                        # User-selected distribution
                        user_dist_name = deepcopy(vrbl_config_dict[vname]['marginal'])


                        # Only trigger PESE-GC code if the ensemble is not entirely degenerate
                        if np.abs( fcst_ens1d.max() - fcst_ens1d.min() ) > 1e-7:

                            # -----------------------------------------------------------------------------------
                            #    PESE CODE HERE
                            # -----------------------------------------------------------------------------------

                            # Step 1: fit MUWE distribution at current location
                            #     Note that the user-specified distribution in muwe is the one specified in 
                            #     config_CAM_pyPESE.py
                            params = all_dist_class_dict['muwe'].fit(
                                fcst_ens1d, all_dist_class_dict[user_dist_name]
                            )
                            fitted_dist = all_dist_class_dict['muwe']( 
                                *params
                            )

                            # Step 2: Transform to probit space
                            fcst_probit1d = std_norm_dist.ppf(
                                fitted_dist.cdf( fcst_ens1d )
                            )

                            # Step 3: Resample in probit space
                            fcst_probit1d -= np.mean( fcst_probit1d)
                            fcst_probit1d /= np.std( fcst_probit1d, ddof=1)                   
                            virt_probit1d = np.matrix( fcst_probit1d[np.newaxis,:]) * E
                            virt_probit1d = np.array(virt_probit1d)[0,:]

                            # Step 4: Transform from probit space to native space
                            virt_ens1d = fitted_dist.ppf(
                                std_norm_dist.cdf( virt_probit1d )
                            )
                            virt_ens1d = virt_ens1d.astype('f8')

                            # Checking for strange values
                            if np.sum( np.isnan( virt_ens1d) + np.isinf(virt_ens1d) ) > 0:
                                msg = "Strange virtual values for %s detected at (%d, %f, %f)" % (
                                    vname, ilvl,
                                    my_xy_ind_dict[gridtype]['y inds'][iloc], my_xy_ind_dict[gridtype]['x inds'][iloc]
                                )
                                msg += "\nfcst_ens1d: " + str( fcst_ens1d)
                                msg += "\nfcst_probit1d: " + str( fcst_probit1d )
                                msg += "\nvirt_probit1d: " + str( virt_probit1d )
                                msg += "\nvirt_ens1d:"  + str( virt_ens1d)
                                mpi_print(msg)
                                mpi_print( virt_probit1d )

                            # Handling negative Q values by zeroing them, then rescale to match the old mean.
                            if vname == 'Q':
                                mean_old = np.mean( virt_ens1d )
                                virt_ens1d_new = deepcopy( virt_ens1d  )
                                virt_ens1d_new[virt_ens1d < 0] = 0
                                mean_new = np.mean(virt_ens1d_new)
                                rescale = mean_old/mean_new
                                virt_ens1d = virt_ens1d_new * rescale

                            # -----------------------------------------------------------------------------------
                            #    PESE CODE ENDS HERE
                            # -----------------------------------------------------------------------------------
                            
                        # --- End of handling case where the ensemble is not entirely degenerate.

                        # Cases where the ensemble is essentially fully degenerate
                        if np.abs( fcst_ens1d.max() - fcst_ens1d.min() ) <= 1e-7:
                            virt_ens1d = np.mean( fcst_ens1d )
                        # --- End of handling fully degenerate ensemble
                        
                        # Pass virtual members back to ens-complete dictionary
                        if gtype_z == 'horizontal':
                            my_ens_complete_data[vname][fcst_ens_size:,iloc] = virt_ens1d
                        else:
                            my_ens_complete_data[vname][fcst_ens_size:,ilvl,iloc] = virt_ens1d
                        # --- end of passing back to ens-complete dictionary
                    
                    # --- End of loop over applicable variables
                # ---End of loop over available levels
            # --- End of loop over available locations
            gc_collect()
        # --- End of loop over vert grid types
    # --- End of loop over horizontal grid types
# --- End of loop over available local noise files
                        

collective_print('Finished applying Localized PESE-GC.\n', comm)





















'''
    TRANSPOSE MEMBERS FROM ENSEMBLE-COMPLETE TO GRID-COMPLETE MODE

    Note: Root process always carries at least one forecast member
'''
collective_print('Transposing virtual members from ensemble-complete to grid-complete.', comm)


collective_print('    Initializing dictionary to hold virtual member data', comm)
# Generate dictionary to hold grid-complete member-varying data arrays
# --------------------------------------------------------------------
my_grid_complete_virt_dict = {}
if my_virt_imem_allotment is not None:
    for imem in my_virt_imem_allotment:
        my_grid_complete_virt_dict[imem] = {}
        my_grid_complete_virt_dict[imem]['variables'] = {}
        for vname in ens_complete_vary_list:
            my_grid_complete_virt_dict[imem]['variables'][vname] = {}
            my_grid_complete_virt_dict[imem]['variables'][vname]['data'] = np.zeros(
                data_info_dict[vname]['shape'], dtype = data_info_dict[vname]['dtype']
            )


collective_print('    Obtaining attribute information for member-varying variables from root process', comm)
# Obtain attribute information from root process
# -----------------------------------------------
for vname in ens_complete_vary_list:

    # Obtain attribute info
    tmp = None
    if my_proc_id == root_proc_id:
        tmp = my_grid_complete_fcst_dict[0]['variables'][vname]['attributes']
    meta = comm.bcast( tmp, root = 0)

    # Put into dictionary
    if my_virt_imem_allotment is not None:
        for imem in my_virt_imem_allotment:
            my_grid_complete_virt_dict[imem]['variables'][vname]['attributes'] = deepcopy(meta)
# --- End of attribute obtainment process


collective_print('    Obtaining member-invariant fields from root process', comm)
# Obtain member-invariant fields from root process
# ------------------------------------------------
for vname in ens_complete_const_list:

    # Obtain data
    tmp = None
    if my_proc_id == root_proc_id:
        tmp = my_grid_complete_fcst_dict[0]['variables'][vname]
    data_arr_and_att = comm.bcast( tmp, root=0 )

    # Shove into dictionary
    if my_virt_imem_allotment is not None:
        for imem in my_virt_imem_allotment:
            my_grid_complete_virt_dict[imem]['variables'][vname] = data_arr_and_att
# End of obtaining invariant fields



collective_print('    Gathering member-varying data', comm)
# Collect member-varying data for virtual members
# -----------------------------------------------
# This MPI gather is done variable & member at a time to limit buffer sizes
# b'cos large buffers take time to allocate.
# Note, to reduce process downtime, will gather all data first to the relevant processes first,
# then remap the gathered data into the regular grid.
# In contrast, doing both the gathering & remapping together results in idling processes.

# Init dictionary to hold gathered data
gather_buf_dict = {}
for vname in ens_complete_vary_list:
    gather_buf_dict[vname] = {}
    

# Loop over all virtual members & variables (this takes three nested loops: vname, iproc & imem)
for vname in ens_complete_vary_list:

    msg = '        ... gathering %s' % (vname)
    collective_print(msg, comm)

    for iproc in np.arange(num_procs):
        
        # Skip if process iproc is not alloted any grid-complete virtual members
        if virt_imem_allotment_for_all_procs[iproc] is None:
            comm.Barrier()
            continue      

        # Loop over virtual members alloted to process iproc
        for imem in virt_imem_allotment_for_all_procs[iproc]:

            # MPI-gather the data to the process iproc
            buf = np.squeeze(my_ens_complete_data[vname][imem])
            gathered_buf = comm.gather( buf, root = iproc )
            # Note: The gathered_buf variable is a list containing domain fragments

            # SAFETY MEASURE: Gather process ids as well
            gathered_proc_ids = comm.gather( my_proc_id, root = iproc)

            # Chuck gathered data & process ids into an easy-to-access dictionary
            if my_proc_id == iproc:
                gather_buf_dict[vname][imem] = {}
                gather_buf_dict[vname][imem]['data'] = gathered_buf
                gather_buf_dict[vname][imem]['proc ids'] = gathered_proc_ids
            # --- End of instruction for iproc

            # Synchronize procs
            comm.Barrier()
        
        # --- End of loop over virtual members allotted to proc iproc

        # Garbage collection to prevent memory overuse due to allocation of buffers
        if iproc % 5 == 0 and iproc > 0:
            gc_collect()


    # --- End of loop over active processes
# --- End of loop over variable naems


# Destroy ensemble complete data to reduce memory use
msg = '    Freeing up memory by deleting ensemble-complete data structures'
collective_print( msg, comm )
del my_ens_complete_data
gc_collect()


# Every virtual member-holding process rearranges data into CAM's grids
collective_print('    Rearranging data fragments into CAM grids', comm)
if my_virt_imem_allotment is not None:
    for imem in my_virt_imem_allotment:
        for vname in ens_complete_vary_list:

            # Detect horizontal grid type
            shp_last2 = data_info_dict[vname]['dimensions'][-2:]
            gtype = iden_gtype[ shp_last2 ]

            # Loop over received fragments
            for ifrag in range(num_procs):

                # Select data fragment & determine grid indices
                iproc = gather_buf_dict[vname][imem]['proc ids'][ifrag]
                data_arr = gather_buf_dict[vname][imem]['data'][ifrag]
                x_inds = all_procs_xy_ind_dict[gtype]['x inds'][iproc]
                y_inds = all_procs_xy_ind_dict[gtype]['y inds'][iproc]

                # Handling 2D situation
                if len( data_info_dict[vname]['shape']) == 2:
                    my_grid_complete_virt_dict[imem]['variables'][vname]['data'][y_inds, x_inds] = data_arr
                
                # Handling 3D situation
                elif len( data_info_dict[vname]['shape']) == 3:
                    my_grid_complete_virt_dict[imem]['variables'][vname]['data'][:, y_inds, x_inds] = data_arr
                
                else:
                    msg = 'ERROR: received virtual member %d data variable %s is neither 2D nor 3D. Exiting.' % (imem+1, vname)
                    mpi_print(msg)
                    comm.Abort()
                # --- End of handling data dimensions
            
            # --- End of loop over received fragments
        # --- End of loop over variables
    # --- End of loop over virtual members alloted to this process
# --- End of escape condition for processes that are not holding onto data

# Reducing memory overhead
msg = '    Freeing up memory by deleting list of data fragments'
collective_print( msg, comm )
del gather_buf_dict
gc_collect()


# Obtain file attributes from root process
# ----------------------------------------
collective_print('    Obtaining netCDF file attributes from root process', comm)
tmp = None
if my_proc_id == root_proc_id:
    tmp = my_grid_complete_fcst_dict[0]['attributes']
ncf_attrs = comm.bcast( tmp, root = root_proc_id )


collective_print('Finished transposing virtual members from ensemble-complete to grid-complete.\n', comm)








