'''
    USEFUL PRINT FUNCTION
'''
# Function to do collective printing (includes an MPI Barrier)
def collective_print( string, comm, root_proc_id = 0 ):

    if comm.rank == root_proc_id:
        print( '(%6.1f secs elapsed) ---- %s' % ( time()-t_start, string ) )
    
    comm.Barrier()

    return


msg = 'Initialized MPI with %d processes\n' % num_procs
collective_print( msg, comm )






'''
    ANOTHER USEFUL PRINT FUNCTION
    
    Note: No need to specify proc_id.
'''
def mpi_print( string, proc_id = my_proc_id ):

    print( 
        'Proc %4d (%6.1f secs elapsed) ---- %s'
        % (
            proc_id, time() - t_start, string
        ) 
    )
    
    return








'''
    PARSING ENSEMBLE CONFIGURATION
'''
collective_print( 'Parsing ensemble_configuration', comm)

# Ensemble sizes
