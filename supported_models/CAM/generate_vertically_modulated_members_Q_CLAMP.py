# Obtain member-invariant fields from root process
# ------------------------------------------------
for vname in ens_complete_const_list:

    # Obtain data
    tmp = None
    if my_proc_id == root_proc_id:
        tmp = my_grid_complete_fcst_dict[0]['variables'][vname]
    data_arr_and_att = comm.bcast( tmp, root=0 )

    # Shove into dictionary
    if my_virt_imem_allotment is not None:
        for imem in my_virt_imem_allotment:
            my_grid_complete_virt_dict[imem]['variables'][vname] = data_arr_and_att
# End of obtaining invariant fields



collective_print('    Gathering member-varying data', comm)
# Collect member-varying data for modulated members
# -----------------------------------------------
# This MPI gather is done variable & member at a time to limit buffer sizes
# b'cos large buffers take time to allocate.
# Note, to reduce process downtime, will gather all data first to the relevant processes first,
# then remap the gathered data into the regular grid.
# In contrast, doing both the gathering & remapping together results in idling processes.

# Init dictionary to hold gathered data
gather_buf_dict = {}
for vname in ens_complete_vary_list:
    gather_buf_dict[vname] = {}
    

# Loop over all modulated members & variables (this takes three nested loops: vname, iproc & imem)
for vname in ens_complete_vary_list:

    msg = '        ... gathering %s' % (vname)
    collective_print(msg, comm)

    for iproc in np.arange(num_procs):
        
        # Skip if process iproc is not alloted any grid-complete modulated members
        if virt_imem_allotment_for_all_procs[iproc] is None:
            comm.Barrier()
            continue      

        # Loop over modulated members alloted to process iproc
        for imem in virt_imem_allotment_for_all_procs[iproc]:

            # MPI-gather the data to the process iproc
            buf = np.squeeze(my_ens_complete_data[vname][imem])
            gathered_buf = comm.gather( buf, root = iproc )
            # Note: The gathered_buf variable is a list containing domain fragments

            # SAFETY MEASURE: Gather process ids as well
            gathered_proc_ids = comm.gather( my_proc_id, root = iproc)

            # Chuck gathered data & process ids into an easy-to-access dictionary
            if my_proc_id == iproc:
                gather_buf_dict[vname][imem] = {}
                gather_buf_dict[vname][imem]['data'] = gathered_buf
                gather_buf_dict[vname][imem]['proc ids'] = gathered_proc_ids
            # --- End of instruction for iproc

            # Synchronize procs
            comm.Barrier()
        
        # --- End of loop over modulated members allotted to proc iproc

        # Garbage collection to prevent memory overuse due to allocation of buffers
        if iproc % 5 == 0 and iproc > 0:
            gc_collect()


    # --- End of loop over active processes
# --- End of loop over variable naems


# Destroy ensemble complete data to reduce memory use
msg = '    Freeing up memory by deleting ensemble-complete data structures'
collective_print( msg, comm )
del my_ens_complete_data
gc_collect()


# Every modulated member-holding process rearranges data into CAM's grids
collective_print('    Rearranging data fragments into CAM grids', comm)
if my_virt_imem_allotment is not None:
    for imem in my_virt_imem_allotment:
        for vname in ens_complete_vary_list:

            # Detect horizontal grid type
            shp_last2 = data_info_dict[vname]['dimensions'][-2:]
            gtype = iden_gtype[ shp_last2 ]

            # Loop over received fragments
            for ifrag in range(num_procs):

                # Select data fragment & determine grid indices
                iproc = gather_buf_dict[vname][imem]['proc ids'][ifrag]
                data_arr = gather_buf_dict[vname][imem]['data'][ifrag]
                x_inds = all_procs_xy_ind_dict[gtype]['x inds'][iproc]
                y_inds = all_procs_xy_ind_dict[gtype]['y inds'][iproc]

                # Handling 2D situation
                if len( data_info_dict[vname]['shape']) == 2:
                    my_grid_complete_virt_dict[imem]['variables'][vname]['data'][y_inds, x_inds] = data_arr
                
                # Handling 3D situation
                elif len( data_info_dict[vname]['shape']) == 3:
                    my_grid_complete_virt_dict[imem]['variables'][vname]['data'][:, y_inds, x_inds] = data_arr
                
                else:
                    msg = 'ERROR: received modulated member %d data variable %s is neither 2D nor 3D. Exiting.' % (imem+1, vname)
                    mpi_print(msg)
                    comm.Abort()
                # --- End of handling data dimensions
            
            # --- End of loop over received fragments
        # --- End of loop over variables
    # --- End of loop over modulated members alloted to this process
# --- End of escape condition for processes that are not holding onto data

# Reducing memory overhead
msg = '    Freeing up memory by deleting list of data fragments'
collective_print( msg, comm )
del gather_buf_dict
gc_collect()


