'''
    SCRIPT TO GENERATE modulated members VIA VERTICAL ENSEMBLE MODULATION
    ====================================================================
    Written by: Man-Yau (Joseph) Chan

    IMPORTANT: Only VERTICAL modulation is applied. Horizontal modulation is left to 
               another day...

    
    Description:
    ------------
        This MPI-enabled Python script applies VERTICAL ensemble modulation onto an ensemble
        of CAM model states to generate modulated members.

        The reference paper for ensemble modulation is:
            Bishop, C., & Hodyss, D. (2009). Ensemble covariances adaptively localized with 
            ECO-RAP. Part 2: A strategy for the atmosphere. Tellus A: Dynamic Meteorology 
            and Oceanography, 61(1), 97-111.
        
        The settings are defined in a file named config_pyPESE_CAM.py


    Assumption about CAM data:
    --------------------------
        All 1D variables and 0D variables are assumed to be the same across
        all ensemble members

        Time dimension is assumed to be singleton (i.e., only 1 time entry)
        

    Software requirements
    ---------------------
        1) Python packages: NumPy, SciPy, NetCDF4 and pyPESE

        2) Number of x-direction * y-direction grid cells does not exceed 
           maximum number of elements in a contiguous dimension of a NumPy 
           array. Typically less than 10^8 elements.     


    Data distribution across processes:
    -----------------------------------
        Data is divvied up among processes in two data layout modes.

        1) State-complete mode:
            Every process holds the entire model state for several ensemble
            members. To be clear, every member is assigned to only 1 process.

        2) Ensemble-complete mode:
            Every process holds a subset of the model state for all ensemble 
            members. To illustrate, suppose there are N original members, V virtual
            members and P processes. The model state is decomposed into P subsets. 
            Process 0 holds state subset 0 for all N+V members, process 1 holds state
            subset 1 for N+V members, and so forth.

        The conversion between data layout modes is called transposition.

        The state subsetting in ensemble-complete mode is generated by the following:
        a) Flatten the state variable array in the x & y directions (if relevant)
        b) Use MPI Scatter to scatter the flattened array across all processes.

        TODO: Use one-sided MPI communication via RMA to perform scattering instead
              of MPI Scatter. One-sided communication is ~40% more efficient than
              MPI scatter. See `compare_bcast_scatter_window.1node010proc.log` for
              comparison with 10 cores on a single node.
'''




