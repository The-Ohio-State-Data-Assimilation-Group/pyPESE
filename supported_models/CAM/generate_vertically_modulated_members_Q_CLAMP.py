    meta = comm.bcast( tmp, root = 0)

    # Put into dictionary
    if my_virt_imem_allotment is not None:
        for imem in my_virt_imem_allotment:
            my_grid_complete_virt_dict[imem]['variables'][vname]['attributes'] = deepcopy(meta)
